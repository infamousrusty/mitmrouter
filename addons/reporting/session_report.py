"""Session Report Addon — Phase 2.

Scans the output directory at shutdown for the artefacts produced by
other addons and renders both an HTML and a Markdown report.

Uses **only** the Python standard library — zero extra dependencies.
"""

from __future__ import annotations

import glob
import json
import os
import textwrap
from datetime import datetime, timezone
from pathlib import Path
from string import Template
from typing import Any

from addons.base import AbstractAddon

